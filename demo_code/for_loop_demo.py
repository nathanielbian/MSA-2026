def main():
    #Print integers between 0 and 10
    for number in range(0, 11):
        print(number)    

    #Print integers between 5 and 10
    print("\n\n")
    for number in range(5, 11):
        print(number)    

    #Print even integers betweeen 0 and 10
    print("\n\n")
    for number in range(0, 10, 2):
        print(number)

main()
